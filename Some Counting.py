"""

Exercise 7: Some Counting

This program uses for loops to print all values.

"""

# Counts up from 0 to 50 in increments of 1

for i in range(51):  # Starts at 0, ends at 50
    print(i)
  
# Counts up from 50 to 0 in decrements of 1
    
for i in range(50, 1, -1):  # Decreases by 1 from 50   
    print(i)
    
# Counts up from 30 to 50 in increments of 1

for i in range(30,51):   # Starts at 30, ends at 50
    print(i)
    
# Counts up from 50 to 10 in decrements of 2
    
for i in range(50, 9, -2):   # Starts at 50, ends at 10, steps of 2
    print(i)
    
# Counts up from 100 to 200 in increments of 5
for i in range(100, 201, 5):   # Starts at 100, ends at 200, steps of 5
    print(i)
    

